my_string = "Hello there!"
print(my_string[1])  # prints only 1 character at that index
print(my_string[-1])  # prints last character
print(my_string[3])
print(my_string[1:3])  # similar to range function
print(my_string[1:6:2])  # again the same
print(my_string[:3])  # range function just given end limit.
print(my_string[:])  # sab kuch print karo
print(my_string[-1::-1])  # by Kaushal
print(my_string[1:])  # phela chhod ke baaki sab
print(my_string[::-1])  # reverse mai print karo
mu = ' Punekar!'
print(my_string + mu)
